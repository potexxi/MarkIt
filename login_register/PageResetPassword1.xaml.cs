        {
            try
            {
                code = new Random().Next(0, 1000000);
                // Code from StackOverFlow
                // search: C# email senden, how to
                SmtpClient client = new SmtpClient("smtp.gmail.com", 587);

                client.UseDefaultCredentials = false;
                client.EnableSsl = true;
                // this is only a fake email, we delete it after the project is finished
                System.Net.NetworkCredential basicauth = new System.Net.NetworkCredential("markit.app.noreply@gmail.com", "kipy tubr chhv qyef");
                client.Credentials = basicauth;
                MailAddress from = new MailAddress("markit.app.noreply@gmail.comm", "MarkIt");
                MailAddress to;
                try
                {
                    to = new MailAddress(emailAddress, "");
                }
                catch
                {
                    MainWindow.logger.Error($"Mailaddress {emailAddress} not correct format.");
                    throw new Exception("mailaddress");
                }
                MailMessage myMail = new MailMessage(from, to);
                myMail.Subject = "Password Reset";
                myMail.SubjectEncoding = Encoding.UTF8;
                // html code generated by ChatGPT
                myMail.Body = $"<!DOCTYPE html><html><head><meta charset='UTF-8'><title>Password Reset</title></head><body style='font-family:Arial,sans-serif;background-color:#f4f4f4;padding:20px;'><table width='100%' cellspacing='0' cellpadding='0'><tr><td align='center'><table width='500' style='background-color:#ffffff;padding:30px;border-radius:10px;'><tr><td align='center'><h2>Password Reset</h2></td></tr><tr><td><p>Hello,</p><p>You requested to reset your password.</p><p>Please use the following verification code:</p></td></tr><tr><td align='center' style='padding:20px;'><div style='font-size:28px;font-weight:bold;letter-spacing:5px;'>{code:D6}</div></td></tr><tr><td><p>This code is valid for a limited time.</p><p>If you did not request this, you can safely ignore this email.</p></td></tr><tr><td style='padding-top:20px;font-size:12px;color:#777;'><p>This is an automated message. Please do not reply.</p></td></tr></table></td></tr></table></body></html>";
