                }
            }
            catch (Exception e)
            {
                if (e.Message == "mailaddress")
                    MessageBox.Show("Something went wrong, check if your email address is in correct format and try again.", "Error",MessageBoxButton.OK, MessageBoxImage.Error);
                else if(e.Message == "sending")
                    MessageBox.Show("Something went wrong while sending the email, try again later.", "Error", MessageBoxButton.OK, MessageBoxImage.Error);
                return false;
            }
